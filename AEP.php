                  <h4><b>RATIONALE</b></h4>
                  <p>&emsp;&emsp;Under Article 40 of the Labor Code of the Philippines, as amended, any alien seeking admission to the Philippines for employment purposes and any domestic or foreign employer who desires to engage an alien for employment in the Philippines shall obtain an employment permit from the Department of Labor and Employment. <br><br>

                    &emsp;&emsp;The Alien Employment Permit (AEP) is a permit issued to a non-resident alien or foreign national seeking admission to the Philippines for employment purposes after a determination of the non-availability of Filipino citizen who is competent, able and willing at the time of application to perform the services for which the alien is desired.<br><br>

                    <b>CLASSIFICATION: </b>SIMPLE<br>
                    <b>Type of Transaction: </b>G2C – Government to Citizen, G2B – Government to Business<br>

                    <b>Who may avail: </b><br>
                    &emsp;&emsp;All foreign nationals who intend to engage in gainful employment in the Philippinesand any domestic or foreign employer who desires to engage an alien for employment in the Philippines<br><br>
                  </p>
